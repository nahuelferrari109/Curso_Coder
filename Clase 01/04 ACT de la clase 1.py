nota_1 = float(input("Ingrese el primer numero: ")); # Pedimos la primer nota
nota_2= float(input("Ingrese el segundo numero: "));# Pedimos la segunda nota 
nota_3 = float(input("Ingrese el tercer numero: "));# Pedimos la tercera nota 

nota_1 = nota_1 * 0.2;  # sacamos el porcentaje de la primera nota es el 20 %.
nota_2 = nota_2 * 0.3; # sacamos el porcentaje de la segunda nota es el 30 %.
nota_3 = nota_3 * 0.5; # sacamos el porcentaje de la tercera nota es el 50 %.

suma = nota_1 + nota_2 + nota_3; # hacemos la suma de los 3 porcentajes.

print("Nota del alumno es: ", suma); # Imprimimos en pantalla las la suma de todo los porcentajes
